package com.example.crimereportsbd;

public class IncidentMedia {
	private int media_type;
	private String media_content;
	/**
	 * @return the media_type
	 */
	public int getMedia_type() {
		return media_type;
	}
	/**
	 * @param media_type the media_type to set
	 */
	public void setMedia_type(int media_type) {
		this.media_type = media_type;
	}
	/**
	 * @return the media_content
	 */
	public String getMedia_content() {
		return media_content;
	}
	/**
	 * @param media_content the media_content to set
	 */
	public void setMedia_content(String media_content) {
		this.media_content = media_content;
	}
	
}
